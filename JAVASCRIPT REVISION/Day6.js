// Strings in JavaScript

// 1. String Creation
let str1 = "Hello, world!";
let str2 = 'Single quotes are also valid';
let str3 = `You can use backticks for multi-line strings`;


// Output Methods 
console.log(str1);
console.log(`String 2: ${str2}`);
console.log(`String 3: ${str3}`);

// String Methods 
let text = "Hello, world!";

// Length of the string
console.log(text.length);

// Accessing characters 
console.log(text[0]); // First character
console.log(text[text.length - 1]); // Last character

// Converting to uppercase and lowercase
console.log(text.toUpperCase());
console.log(text.toLowerCase());

// Searching for a substring    
let search = "world";
console.log(text.indexOf(search)); // First occurrence
console.log(text.lastIndexOf(search)); // Last occurrence




