"use strict"; // it is used to prevent the use of undeclared variables
// Premetive Data Types

// 1. Number
// 2. String
// 3. Boolean
// 4. Null
// 5. Undefined
// 6. Symbol

// These are the most common premitive data types in javascript

// 1. Number
let a = 10;
console.log(a);

// 2. String
let b = "Hello";
console.log(b);

// 3. Boolean
let c = true;
console.log(c);

// 4. Null
let d = null;
console.log(d);

// 5. Undefined
let e;
console.log(e);

// 6. Symbol
let f = Symbol("Hello");
console.log(f);


// to find data type of a variable we use typeof operator

console.log(typeof a);
console.log(typeof b);
console.log(typeof c);
console.log(typeof d);
console.log(typeof e);
console.log(typeof f);

